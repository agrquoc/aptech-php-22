     */
    public function store(Request $request)
    {
        {
            $article = new Article();
            $slug=$request->title;
            $article->title = $request->title;
            $article->slug = str_slug($slug,'-');
            $article->description = $request->description;
            $article->save();
            return redirect()->route('articles.index');
        }
    }

